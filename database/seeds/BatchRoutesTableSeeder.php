	public function run ()
	{
		foreach ( $this->routes as $value ) {
			$batch_route = new BatchRoute();
			$batch_route->batch_code = $value[0];
			$batch_route->batch_route_name = $value[1];
			$batch_route->batch_max_units = $value[2];
			$batch_route->export_template = $value[4] ?: null;
			$batch_route->batch_options = $value[5];
			$batch_route->save();

			$original_stations = $value[3];

			$stations_to_add = \App\Station::whereIn('station_name', $original_stations)
								  ->orderByRaw(sprintf("FIELD (station_name, '%s')", implode("', '", $original_stations)))
								  ->lists('id')
								  ->toArray();

			$batch_route->stations()
						->attach($stations_to_add);
		}
	}
}
